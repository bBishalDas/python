marksin=int(input("enter your marks here: "))

if 90<=marksin<=100:
    print("your grade:  EX")
elif 80<=marksin<=90:
    print("your grade: A")
elif 70<=marksin<=80:
    print("your grade: B")
elif 60<=marksin<=70:
    print("your grade: C")
elif 50<=marksin<=60:
    print("your grade: D")
else:
    print("your grade: F")